%D calculator
function [D] = Damp(u1,N,e1,e2)

for i = 3:N-2
    D(i) = e1*(u1(i+1) - 2*u1(i) + u1(i-1)) - e2*(u1(i+2) - 4*u1(i+1) + 6*u1(i) - 4*u1(i-1) + u1(i-2));
end

D(1) = e1*(u1(2) - 2*u1(1) + u1(1)) - e2*(u1(3) - 4*u1(2) + 6*u1(1) - 4*u1(1) + u1(1));
D(2) = e1*(u1(3) - 2*u1(2) + u1(1)) - e2*(u1(4) - 4*u1(3) + 6*u1(2) - 4*u1(1) + u1(1));
D(N-1) = e1*(u1(N) - 2*u1(N-1) + u1(N-2)) - e2*(u1(N) - 4*u1(N) + 6*u1(N-1) - 4*u1(N-2) + u1(N-3));
D(N) = e1*(u1(N) - 2*u1(N) + u1(N-1)) - e2*(u1(N) - 4*u1(N) + 6*u1(N) - 4*u1(N-1) + u1(N-2));
end